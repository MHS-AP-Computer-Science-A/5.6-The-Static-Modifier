
public class Main { //Every method and instance variable in this class
	                //MUST BE STATIC so that they exist. We will never create
	                //a new object of this type

	public static void main(String[] args) {
		
		//If a class method is static, we can call it without making an 
		//object - we just use the class name 
		System.out.println(Math.sqrt(400));
		System.out.println(Math.abs(-55));
		System.out.println(Math.pow(4, 2));


	}

}
